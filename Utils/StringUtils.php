     * Nettoie une chaine de caractère composée d'accents
     * 
     * @param $string
     * @param string $encId
     *
     * @return string
     */
    public static function regexAccents($string, $encId = 'UTF-8')
    {
        setlocale(LC_ALL, 'fr_BE.utf8');
        return iconv($encId, 'ASCII//TRANSLIT', $string);

    }

