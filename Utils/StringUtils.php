    /**
     * Nettoie le nom d'un fichier.
     *
     * Exemple :
     * Modèle d'échange (v1.2).docx
     * Devient : Modele_d_echange_v1.2.docx
     * 
     * @author Christian Mattart <christian.mattart@lesscouts.be>
     *
     * @param string $filename
     *
     * @return string
     * @throws \Exception
     */
    public static function sanitizeFilename($filename)
    {
        $cleanFilename = null;

        // On ne traite pas le chemin, uniquement le nom du fichier.
        if (strpos($filename, '/') !== false)
        {
            throw new \Exception(sprintf('Filename "%s" should not contain full path, only filename. Forward slashes are illegal in filenames.', $filename));
        }

        // Si le nom du fichier est déjà clean, on le retourne.
        if (self::isFilenameClean($filename))
        {
            return $filename;
        }

        $cleanFilename = self::removeAccents($filename);

        $lookFor = array(
            '/[^-_\.a-z0-9]/i', // Tout ce qui n'est pas alphanumérique ou -_.
            '/_[a-z]_/i', // Les lettres seules (issues d'apostrophes)
            '/_+/', // Les _ multiples
            '/_\./', // L'éventuel _ avant l'extension
        );
        $replaceWith = array(
            '_',
            '_',
            '_',
            '.',
        );
        $cleanFilename = preg_replace($lookFor, $replaceWith, $cleanFilename);

        if (null === $cleanFilename)
        {
            throw new \Exception(sprintf('Filename could not be parsed.'));
        }

        return $cleanFilename;
    }

    /**
     * Indique si le nom de fichier $filename ne contient que des caractères valides.
     *
     * Les caractères acceptés sont :
     * - caractères alphanumériques
     * - point
     * - underscore
     * - trait d'union
     * 
     * @param string $filename
     * @return boolean
     */
    public static function isFilenameClean($filename)
    {
        // Si le nom du fichier est déjà clean, on retourne true
        return (preg_match('/^[-_\.a-z0-9]+$/i', $filename) === 1);
    }

